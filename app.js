function buildRouteThread(digest = routeDigest(), archiveNumber = 0) {
  const tones = digest
    .filter((tone) => endingRouteLabels[tone] && endingRouteNotes[tone])
    .slice(0, 2);

  if (!tones.length) {
    return "封底只剩一道浅痕，像未记录的停顿终于获得了座位。";
  }

  return tones
    .map((tone, index) => {
      const notes = endingRouteNotes[tone] || endingRouteNotes.official;
      return notes[(archiveNumber + index) % notes.length];
    })
    .join("");
}

